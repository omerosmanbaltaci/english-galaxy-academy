let currentLang = localStorage.getItem('site_lang') || 'en';

function applyTranslations() {
    if (typeof translations === 'undefined') return;
    const dict = translations[currentLang];
    if (!dict) return;
    
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (dict[key]) {
            if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                if (el.hasAttribute('placeholder')) {
                    el.setAttribute('placeholder', dict[key]);
                }
            } else {
                el.innerHTML = dict[key]; // innerHTML allows bold/span tags if needed
            }
        }
    });
    
    // Update Toggle Button Text to show CURRENT language text
    const pillBtn = document.getElementById('lang-toggle-btn');
    if (pillBtn) {
        if (currentLang === 'en') {
            pillBtn.classList.remove('tr-active');
            pillBtn.classList.add('en-active');
        } else {
            pillBtn.classList.remove('en-active');
            pillBtn.classList.add('tr-active');
        }
    }
}

function toggleLanguage() {
    currentLang = currentLang === 'en' ? 'tr' : 'en';
    localStorage.setItem('site_lang', currentLang);
    applyTranslations();
}

// Sync language changes across multiple open browser tabs
window.addEventListener('storage', (event) => {
    if (event.key === 'site_lang') {
        currentLang = event.newValue || 'en';
        applyTranslations();
    }
});

// Run on page load
document.addEventListener('DOMContentLoaded', () => {
    applyTranslations();
});
